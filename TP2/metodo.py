class Metodo():
    def __init__ (self, numero,detalles):
        self.numero=numero
        self.detalles=detalles
    def get_numero(self):
        return self.numero
    def get_detalles(self):
        return self.detalles
    def set_numero(self,numero):
        self.numero=numero
    def set_detalles(self,detalles):
        self.detalles=detalles

